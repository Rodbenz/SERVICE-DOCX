SELECT CASE  
WHEN T1.BUILD_NAME = '-' THEN ''
ELSE T1.BUILD_NAME END BUILD_NO, 
T1.OFLEVEL, T1.OFLEVEL_ORDER, T1.INDEX_ADJ, T1.VAL_AMT_P_MET, T1.USE_CATG, T1.FlOOR_TYPE_ID, T1.FLOOR_VAL_ID
FROM (
SELECT VAL_AMT_P_MET, M_FLOOR, INDEX_ADJ, FLOOR_VAL_ID, CONDO_S_ID, CONDO_B_ID, CONDO_L_ID, BUILD_NAME, OFLEVEL, OFLEVEL_ORDER, USE_CATG, FlOOR_TYPE_ID
FROM condo.dbo.CFLOOR_VAL
WHERE CONDO_S_ID = @CONDO_S_ID AND PERIODS_ID IS NULL
) T1
WHERE RTRIM(LTRIM(OFLEVEL)) NOT IN ('ชั้นดาดฟ้า', 'ดาดฟ้า')
ORDER BY T1.FLOOR_VAL_ID